from math import sqrt

a = 20
b = 15

# Находим среднее арифметическое значение a и b
print(f'Среднее арифметическое значение чисел a={a} и b={b} равно {((a + b)/2)}')

# Находим среднее геометрическое значение a и b
print(f'Среднее геометрическое значение чисел a={a} и b={b} равно {(sqrt(a * b))}')